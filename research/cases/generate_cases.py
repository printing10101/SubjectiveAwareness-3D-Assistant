def clean_text(text):
    """Replace ASCII double quotes inside Chinese text with full-width quotes.
    This avoids syntax issues when embedding text in Python string literals."""
    result = []
    in_quote = False
    for ch in text:
        if ch == '"':
            if in_quote:
                result.append("\u201d")
                in_quote = False
            else:
                result.append("\u201c")
                in_quote = True
        else:
            result.append(ch)
    return "".join(result)


def q(text):
    """Wrap text in single quotes for Python string safety."""
    return clean_text(text)


