def main():
    """生成扩充数据集"""
    random.seed(42)

    # 目标：生成100条样本，三类均衡分布
    # 明知: 40条, 不明知: 30条, 边缘: 30条
    target_positive = 40
    target_negative = 30
    target_edge = 30

    samples = []

    print("生成明知类样本...")
    for i in range(target_positive):
        sample = generate_positive_sample()
        samples.append(sample)

    print("生成不明知类样本...")
    for i in range(target_negative):
        sample = generate_negative_sample()
        samples.append(sample)

    print("生成边缘类样本...")
    for i in range(target_edge):
        sample = generate_edge_sample()
        samples.append(sample)

    # 去重检查
    seen = set()
    unique_samples = []
    duplicates = 0
    for sample in samples:
        content_hash = hashlib.md5(sample["input"].encode()).hexdigest()
        if content_hash not in seen:
            seen.add(content_hash)
            unique_samples.append(sample)
        else:
            duplicates += 1

    if duplicates > 0:
        print(f"去重：移除 {duplicates} 条重复样本")

    # 保存
    project_root = Path(__file__).parent.parent
    raw_dir = project_root / "data" / "raw"
    training_dir = project_root / "data" / "training"

    raw_dir.mkdir(parents=True, exist_ok=True)
    training_dir.mkdir(parents=True, exist_ok=True)

    # 保存为processed.jsonl（模拟数据处理后的输出）
    output_file = training_dir / "processed.jsonl"
    with open(output_file, "w", encoding="utf-8") as f:
        for sample in unique_samples:
            f.write(json.dumps(sample, ensure_ascii=False) + "\n")

    # 保存原始数据格式（模拟爬虫输出）
    for i, sample in enumerate(unique_samples):
        raw_record = {
            "case_id": f"CASE_{i:04d}",
            "title": f"帮信罪案例{i + 1}",
            "court": "",
            "case_number": f"(2024)黔刑初{i + 1:03d}号",
            "judgment_date": "2024-01-15",
            "case_type": "帮助信息网络犯罪活动罪",
            "content": sample["input"],
            "source_url": "",
            "crawl_time": datetime.now().isoformat(),
            "content_hash": hashlib.md5(sample["input"].encode()).hexdigest(),
        }
        raw_file = raw_dir / f"CASE_{i:04d}.json"
        with open(raw_file, "w", encoding="utf-8") as f:
            json.dump(raw_record, f, ensure_ascii=False, indent=2)

    # 保存爬取统计
    crawl_stats = {
        "total_fetched": len(unique_samples),
        "success_count": len(unique_samples),
        "fail_count": 0,
        "duplicate_count": duplicates,
        "errors": [],
    }
    with open(raw_dir / "crawl_stats.json", "w", encoding="utf-8") as f:
        json.dump(crawl_stats, f, ensure_ascii=False, indent=2)

    # 统计
    print("\n生成完成：")
    print(f"  总样本数: {len(unique_samples)}")
    print(f"  明知类: {target_positive}")
    print(f"  不明知类: {target_negative}")
    print(f"  边缘类: {target_edge}")
    print(f"  去重: {duplicates}")
    print("\n保存位置:")
    print(f"  原始数据: {raw_dir}")
    print(f"  处理数据: {output_file}")


if __name__ == "__main__":
    main()
